    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       
        Validation validation = new Validation();
        //check if any fields are empty
        if (validation.isFieldEmpty(txtID, txtName,txtLName,txtDes,txtEmail,txtMob,txtSal) > 0) {
            JOptionPane.showMessageDialog(this, "All fields are required!!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
          int id=Integer.parseInt(txtID.getText());
          if (!txtID.equals("")) {
            try {
                String query="DELETE from employee where id=?";
                PreparedStatement pst1=conn.prepareStatement(query);
                pst1.setInt(1,id);
                pst1.executeUpdate();
                
                 JOptionPane.showMessageDialog(this, "Employee Deleted Successfully!!", "Confirm", JOptionPane.INFORMATION_MESSAGE);   
                 reset();
                 setVisible(false);
                 new Home().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Employee with this ID does not exist!!", "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
          else
          {
              JOptionPane.showMessageDialog(this, "Please enter employee id!!", "Error", JOptionPane.ERROR_MESSAGE);
          }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

