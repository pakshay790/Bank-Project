    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
      String id=txtID.getText();
      Validation validation = new Validation();
        //check if any fields are empty
        if (validation.isFieldEmpty(txtEmail,txtDes,txtLName,txtMob,txtName,txtSal) > 0) {
            JOptionPane.showMessageDialog(this, "All fields are required!!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if (!validation.isNumber(txtMob.getText()) || txtMob.getText().length() < 10) {
            JOptionPane.showMessageDialog(this, "Incorrect Mobile number!!!", "Error", JOptionPane.ERROR_MESSAGE);
        }else
        {
            if (txtID.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter employee id.!!!", "Error", JOptionPane.ERROR_MESSAGE);
            } 
            else {
                try {
                    PreparedStatement pst2=conn.prepareStatement("select * from employee where id=?");
                    pst2.setString(1,id);
                    rs=pst2.executeQuery();
                    if(rs.next())
                    {
                        JOptionPane.showMessageDialog(this, "Employee id already exist!!!", "Error", JOptionPane.ERROR_MESSAGE);
                       
                    }
                    else
                    {
                        PreparedStatement pst3=conn.prepareStatement("insert into employee(id,name,surname,designation,mobile_no,email,salary) values(?,?,?,?,?,?,?)");
                        pst3.setString(1,id);
                        pst3.setString(2,txtName.getText());
                        pst3.setString(3,txtLName.getText());
                        pst3.setString(4,txtDes.getText());
                        pst3.setString(5,txtMob.getText());
                        pst3.setString(6,txtEmail.getText());
                        pst3.setString(7,txtSal.getText());
                        
                        
                        
                        if(pst3.executeUpdate()==1)
                        {
                            JOptionPane.showMessageDialog(this, "Employee Added Successfully", "Successfull", JOptionPane.INFORMATION_MESSAGE);
                            reset();
                            setVisible(false);
                            new Home().setVisible(true);
                        }
                    }
                    
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
      
    }//GEN-LAST:event_btnAddActionPerformed

